import { FC } from 'react';
import { BrowserRouter, Route, Routes } from 'react-router-dom';

import ROUTES from './config';

const Router: FC = () => (
  <BrowserRouter>
    <Routes>
      {ROUTES.map(({ path, element }) => (
        <Route path={path} element={element} key={path} />
      ))}
    </Routes>
  </BrowserRouter>
);

export default Router;
